		$this->Page_DataRendering($sHeader);
		$Doc->Text .= $sHeader;
		$this->ExportDocument($Doc, $rs, $this->StartRec, $this->StopRec, "view");

		// Export detail records (main_PartNum)
		if (EW_EXPORT_DETAIL_RECORDS && in_array("main_PartNum", explode(",", $this->getCurrentDetailTable()))) {
			global $main_PartNum;
			if (!isset($main_PartNum)) $main_PartNum = new cmain_PartNum;
